     Title: RUCCOLASALAT MIT GEBACKENER ZIEGENKÄSEPRALINE
Categories: Vorspeise, Salat, Käse, Chefkoch, Zacherl
     Yield: 4 Personen

      6 bn Ruccola
      6 tb Balsamico-Essig
      2 tb Pinienkerne
    300 ml Passierte Dosentomaten
      6 tb Olivenöl
      1    Zehe Knoblauch
           Salz
           Pfeffer
           Zucker
      1 ts Mittelscharfer Senf
      6    Blätter Basilikum

MMMMM-----------ZIEGENKÄSEPRALINE----------------
    200 g  Ziegenfrischkäse in Rollen
      3 sl Toast
      1    Zweig Rosmarin
      1    Knoblauchzehe
      4    Blätter Frühlingsrollenteig
           Salz
      1 ts Cayennepfeffer
           Parmesan

MMMMM-----------------QUELLE----------------------
           -- Ralf Zacherl "Berliner
           -- Meisterkoch 2002"
           -- Erfasst *RK* 25.08.04 von
           -- Peter Mess

Ruccolasalat mit Tomatenvinaigrette mit gebackener Ziegenkäsepraline

Ruccolasalat von den dicken Stielen befreien und in kaltem Wasser
waschen.  Salat abtropfen.

Pinienkerne in einer Pfanne rösten und salzen.

Dressing: In eine Schüssel Pizzatomaten, 1TL Senf, Balsamico,
gehackten Knoblauch, Salz, Pfeffer und Zucker geben. Mit einem
Schneebesen verrühren. Unter ständigem Rühren das Olivenöl zugeben.
Abschmecken.

Ziegenkäsepraline

Den Ziegenfrischkäse zerbröckelt in eine Schüssel geben. Toastbrot
zu feinen Bröseln mixen. Ei, Rosmarin, Knoblauch, Salz und
Cayennepfeffer hinzugeben. Alles verrühren und das gemixte Toastbrot
unterrühren. Die Masse ist jetzt fertig und man lässt sie 15 min.
ruhen.

Frühlingsrollenblätter vierteln. Ein EL Füllung einfüllen. Säckchen
formen und mit Alufolie schließen.

Die Ziegenkäsesäckchen werden bei 160°C - 180°C im Umluftofen 8 - 10
min. goldgelb gebacken.

In der Zwischenzeit wird der Ruccola auf einem Teller angerichtet
und Parmesan darüber gehobelt. Basilikum in Streifen schneiden und
zum Dressing hinzugeben. Ziegenkäsesäckchen aus dem Ofen nehmen und
auf dem Salatteller anrichten. Pinienkerne auf den Salat streuen.

