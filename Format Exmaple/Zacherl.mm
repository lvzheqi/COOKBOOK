     Title: CORDON BLEU MAL ANDERS
Categories: Fleisch, Geflügel, Pute, Chefkoch, Zacherl
     Yield: 4 Personen

      4    Dicke Putenschnitzel
      2 tb Mehl
      1    Zweig Rosmarin
    200 g  Taleggio Käse
      6 sl Toast ohne Rinde
      2    Zweige Thymian
      8 sl Serrano-Schinken
      2    Eier
      1 sm Bund Blattpetersilie
           Salz
           Pfeffer

MMMMM-----------------QUELLE----------------------
           -- Ralf Zacherl "Berliner
           -- Meisterkoch 2002"
           -- Erfasst *RK* 25.08.04 von
           -- Peter Mess

Toastbrot entrinden und in einen Mixer geben. Rosmarin, Thymian und
Blattpetersilie abzupfen und in den Mixer geben. (Ohne Stiele)
Zutaten ganz fein mixen, so dass das Toastbrot grün wird. Pute
leicht plattieren. Tasche einschneiden.

Füllung vorbereiten:

Taleggio in 4 Stücke schneiden.

In die Tasche im Fleisch eine Scheibe Serrano legen. Auf den Serrano
kommt ein Stück Tallegio und darauf noch mal eine Scheibe Serrano.
Dann das Fleisch zuklappen.

Eier in eine Schüssel geben und verrühren.

Mehl auf einen Teller.

Toastbrotbrösel in eine Schüssel geben.

Und jetzt geht's zum panieren: Gefülltes Putenschnitzel in Mehl
wenden, dann durch die Eier ziehen und dann in den Toastbrotbröseln
wenden und richtig panieren bis keine Löcher mehr da sind (Tipp:
sind noch Löcher da, den ganzen Vorgang wiederholen.)

Pfanne auf den Herd stellen. Normales Pflanzenöl in die Pfanne.
Soviel, dass das Putenschnitzel schwimmen kann.

Pfanne gut erwärmen, aber nicht zu heiß, sonst verbrennt das gute
Schnitzel.

Putenschnitzel in die Pfanne und ca. 8 - 10 Minuten braten und bei
dem Vorgang öfter wenden.

Tipp: Während der letzten 4 Minuten 1 Esslöffel Margarine hinzugeben.
Durch das Wasser in der Margarine geht der Garvorgang schneller.

Aus der Pfanne nehmen und auf einem Küchenpapier abtropfen lassen
und anrichten. Ideal passen dazu Spaghetti mit einer Knoblauch-
Tomatensauce.

Tipp: Zwischendurch immer mal das heiße Fett mit einem Löffel über
das Schnitzel geben. So wird das Fleisch von allen Seiten
gleichmäßig gebraten und schön knusprig.

