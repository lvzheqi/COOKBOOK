     Title: TANDOORI CHICKEN MIT EIERNUDELN
Categories: Geflügel, Huhn, Asien, Chefkoch, Zacherl
     Yield: 1 Rezept

      4    Hähnchenschenkel
      1 tb Rotes Tandoori
    450 g  Mie-Nudeln (chinesische
           -- Eiernudeln)
      2 bn Frühlingslauch
           Sojasauce
           Öl
           Salz
           Pfeffer

MMMMM-----------------QUELLE----------------------
           -- Ralf Zacherl "Berliner
           -- Meisterkoch 2002"
           -- Erfasst *RK* 25.08.04 von
           -- Peter Mess

Hühnerschenkel auslösen, dann in kleine Stücke schneiden, mit
Tandooripaste marinieren und durchziehen lassen.

Nudelwasser aufsetzen, leicht salzen und zum Kochen bringen.
Frühlingslauch putzen und waschen und in feine Ringe schneiden Wok
aufsetzen und etwas Öl hineingeben und darin die marinierten
Hühnerwürfel anbraten. Vorsicht: die Hitze darf nicht zu hoch sein,
denn im Tandoori ist Zucker und der karamellisiert bei zu hoher
Hitze, dann wird das Fleisch schwarz.

Ist das Fleisch gar, nach ca. 8 min. auf einen Teller geben. Im Ofen
bei 80°C warm halten.

Nudeln ins kochende Wasser geben und den Wok wieder auf den Herd,
die feinen Zwiebellauchringe hineingeben und mit Salz und Pfeffer
abschmecken, dann mit ca. 3 EL Sojasauce ablöschen.

Und 100 bis 150 ml heißes Nudelwasser zum Zwiebellauch dazu.

Nudeln abschütten, in den Wok geben, durchschwenken, 1 Minute
durchziehen und anrichten.

Hühnerfleisch obendrauf und es sich schmecken lassen

