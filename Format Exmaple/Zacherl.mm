Zwiebeln würfeln und Knoblauch hacken. Die Zwiebelwürfel und den
gehackten Knoblauch in Öl anschwitzen, die Gemüsereste der
Gemüsesticks von der Vorspeise dazugeben und mit Currypaste und
Gewürzen (Chili, Salz, Pfeffer) anwürzen.

Wenn man keine Dips vorher machen möchte, einfach 1 Staudensellerie,
1 Mohrrübe, Lauch und Fenchel klein schneiden und dazugeben.

Sind die Gemüse glasig geschwitzt, mit Sojasauce ablöschen und mit
Geflügelbrühe und Tomaten aus der Dose auffüllen. Die Kaffirblätter
