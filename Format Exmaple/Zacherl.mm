     Title: RINDERFILETSTREIFEN MIT KOKOS-LIMETTEN-MEERRETTICHSAUCE
Categories: Fleisch, Rind, Chefkoch, Zacherl
     Yield: 4 Personen

    600 g  Rinderfilet
      3 tb Öl zum Braten
           Salz
           Pfeffer
           Cayenne

MMMMM------KOKOS-LIMETTEN-MEERREETICHSA-----------
     60 g  Meerrettich
    150 g  Mayonaise
  1 1/2 tb Kokosraspel geröstet
      1    Limette, Abrieb und Saft
           Salz
           Pfeffer
           Zucker nach Geschmack
    150 g  Joghurt

MMMMM------------MANIOKPLÄTZCHEN-----------------
      2    Eigelb
      4 tb Semmelbrösel
    1/2 bn Schnittlauch
    500 g  Maniok
           -- oder
    400 g  Kartoffeln (und 4 EL
           -- Speisestärke)
           Salz
           Muskat
      2 tb Butter

MMMMM-----------------QUELLE----------------------
           -- Ralf Zacherl "Berliner
           -- Meisterkoch 2002"
           -- Erfasst *RK* 25.08.04 von
           -- Peter Mess

Rinderfiletstreifen mit kalter Kokos-Limetten-Meerrettichsauce dazu
Maniok-Schnittlauch-Plätzchen

Rinderfilet parieren (von Sehnen und Fett befreien), in Streifen
schneiden und würzen. Eine Pfanne auf den Herd stellen, gut erhitzen
und nun Öl dazu geben. Darin das Fleisch ca. 2-4 Minuten scharf
braten. Kurz auf ein Küchenpapiertuch geben und dann schön auf einen
Teller anrichten.

Dazu reichen wir leckere Saucen und Plätzchen.

Kalte Kokos-Limetten-Meerreetichsauce:

Alle Zutaten in eine Schüssel geben, verrühren und abschmecken

Maniokplätzchen

Den Maniok schälen und ca. 45 min. in gesalzenem Wasser kochen. Wenn
er weich gekocht ist, den Maniok klein hacken und stampfen oder
durch eine Kartoffelpresse drücken. Dann alle Zutaten in eine
Schüssel und zu einem Teig verrühren. Plätzchen formen (wie
Kartoffelplätzchen) und in Butter ausbacken.

Wenn man anstatt Maniok Kartoffeln benutzt sollte man noch 4 EL
Speisestärke hinzufügen, dann sind die Plätzchen fester.

