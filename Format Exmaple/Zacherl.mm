     Title: ZANDER AUF KÜRBISKRAUT
Categories: Fisch, Zander, Kürbis, Chefkoch, Zacherl
     Yield: 4 Personen

    600 g  Zanderfilet mit Haut
      8 md Kartoffeln
    500 g  Muskatkürbis geschält
      4    Peperoni
      5    Zweige Rosmarin
      4 tb Creme Fraiche
      2 tb Honig
      1 ts Ingwer
      2    Knoblauchzehen
      2 tb Butter
      2 tb Balsamico
           Mehl
           Salz
           Pfeffer

MMMMM-----------------QUELLE----------------------
           -- Ralf Zacherl "Berliner
           -- Meisterkoch 2002"
           -- Erfasst *RK* 25.08.04 von
           -- Peter Mess

Topf mit Wasser aufsetzen. Kartoffeln waschen, schälen und halbieren.
Sobald das Wasser kocht, die Kartoffeln ca. 15 min. kochen, bis sie
einen guten Biss haben.

Kürbis grob reiben und einsalzen, dann auf die Seite stellen.
Zanderfilet prüfen, ob alle Schuppen weg sind. Genau nachschauen, ob
alle Gräten im Filet entfernt sind. In ca. 150 g - Stücke schneiden.

Peperoni entkernen und in kleine Würfel schneiden. Ingwer schälen
und hacken, ca. 1TL Knoblauch schälen und klein schneiden. Rosmarin
abzupfen und die Nadeln klein schneiden.

Topf aufsetzten, Butter hineingeben, Knoblauch anschwitzen, Ingwer
und Peperoni hinzugeben und ein wenig mitschwitzen lassen. Den
ausgedrückten geriebenen Kürbis hinzugeben. Mit Salz und Pfeffer
abschmecken. Als letztes Honig hinzu und noch eine weitere Minute
andünsten.

Eine große Pfanne, in die der Fisch und die Kartoffeln passen, auf
den Herd stellen und heiß werden lassen. Wenn so eine große Pfanne
nicht im Haus ist, dann ruhig zwei Pfannen benutzen.

Öl in die Pfanne, dann die Kartoffeln mit der Schnittfläche zuerst
rein und ein wenig anbraten. Zander salzen und pfeffern und mit der
Hautseite in die Pfanne legen. Auf den Zander einen kleinen Teller
stellen, damit beim Braten die ganze Haut kross wird.

Kartoffeln ab und zu wenden.

Wenn die Hautseite des Zanders braun ist, den Teller wegnehmen und
wenden. Die zweite Knoblauchzehe andrücken und dazugeben.

Butter dazugeben und den Fisch mit der flüssigen Butter übergießen.
Diesen Vorgang nennt man unter Köchen "napieren".

Am Ende den Rosmarin hinzugeben und alles kurz durchschwenken. Nun
Kürbiskraut auf den Herd stellen und Creme Fraiche und Balsamico
dazugeben und einmal aufkochen.

Fisch, Kartoffeln und Kürbiskraut auf einen Teller schön anrichten
und dann genießen.

Tipps & Tricks:

Je kleiner die Peperoni sind, desto schärfer sind sie.

Wem die Pfanne zu klein ist und man keine zweite hat, hier noch ein
Tipp: erst die Kartoffeln goldbraun anbraten, den Rosmarin zugeben
und alles auf einen Teller geben und im Ofen warm halten. Dann in
der gleichen Pfanne den Zander braten.

