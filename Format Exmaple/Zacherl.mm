Knoblauch dazugeben mit Kräutern und Gewürzen abschmecken.

In der Zeit 100 ml Sahne + Eier zusammen anquirlen und salzen. Nach
5 min. Minisalami dazugeben, die angequirlten Eier darüber gießen,
mit Käsestreifen bestreuen und im Backofen bei 160 °C 10 - 15 min.
