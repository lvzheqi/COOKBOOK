           -- Meisterkoch 2002"
           -- Erfasst *RK* 25.08.04 von
           -- Peter Mess

Die Kartoffelscheiben gut abtropfen lassen. Das Olivenöl in eine
Pfanne geben und die Kartoffelscheiben darin 1 min. scharf anbraten,
