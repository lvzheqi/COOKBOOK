     Title: CRUNCHY LASAGNE MIT MANGO UND VANILLEEIS
Categories: Dessert, Teigware, Chefkoch, Zacherl
     Yield: 4 Personen

      4    Blätter Frühlingsrollenteig
      1    Mango
      1    Ei
    100 ml Orangensaft
      4    geh. TL Weißer Sesam
      1 sm Chilischote rot
    500 g  Vanilleeis

MMMMM-----------------QUELLE----------------------
           -- Ralf Zacherl "Berliner
           -- Meisterkoch 2002"
           -- Erfasst *RK* 25.08.04 von
           -- Peter Mess

Mango oben und unten abschneiden (damit man sie stellen kann). Mit
einem Sparschäler schälen. Hochkant die zwei schönen Stücke längs
vom Kern und das restliche Fleisch vom Kern lösen.

Aus den beiden schönen Stücken Spalten schneiden, den Rest des
Mangofleischs in einen Mixer, dann Chilischote kleingeschnitten und
O-Saft hinzu, gegebenenfalls Zucker, und nun fein mixen.

Frühlingsrollenteig vierteln und dann in Dreiecke oder Vierecke
schneiden. Das Ei aufschlagen und verrühren, den Frühlingsrollenteig
mit einem Pinsel mit dem Ei bestreichen, mit Sesam bestreuen und bei
160 - 170 °C 5 bis 8 Minuten goldbraun im Ofen backen.

In der Zwischenzeit das Eis aus der Gefriertruhe nehmen und antauen
lassen, dann kann man es besser portionieren.

Die Mangospalten fächerförmig anrichten. Die Mango-Chilisauce über
die Mangospalten gießen und ein paar Tupfen auf den Teller verteilen.
Mit einem Eßlöffel Vanilleeis abstechen und in die Mitte geben.
Darauf ein Stück vom knusprig gebackenen Frühlingsrollenteig, dann
wieder eine Schicht Vanilleeis usw.. Das ganze viermal wie eine
Lasagne schichten. Wer möchte, mit einem Minzblatt dekorieren. Und
schnell essen!

