     Title: ANANAS- UND SCHAFSKÄSE-BULETTEN
Categories: Snack, Käse, Chefkoch, Zacherl
     Yield: 4 Personen

MMMMM------------ANANAS-BULETTEN-----------------
    600 g  Gemischtes Hackfleisch
    100 g  Ananas
      1    Zwiebel
      3    Semmelknödel (Fertigmasse)
      6 ts Currypulver
      3 tb Semmelbrösel
      4 ts Kurkuma

MMMMM----------SCHAFSKÄSE-BULETTEN---------------
    600 g  Gemischtes Hackfleisch
    100 g  Schafskäse
      1    Zwiebel
      3    Semmelknödel (Fertigmasse)
      4    Zweige Thymian
      2    (-3) Knoblauchzehen
      1    Zweig Rosmarin

MMMMM-----------------QUELLE----------------------
           -- Ralf Zacherl "Berliner
           -- Meisterkoch 2002"
           -- Erfasst *RK* 25.08.04 von
           -- Peter Mess

Ananas Buletten

Semmelknödel laut Packungsangabe einweichen. Nicht aus den einzelnen
Verpackungen herausnehmen.

Fleisch in eine Schüssel geben und mit Salz und Pfeffer gut
verrühren.

100g Ananas in kleine Würfel schneiden. Zwiebel schälen und in
Würfel schneiden. Alle Zutaten zum Fleisch geben (Ananas,
Zwiebelwürfel, Curry, Kurkuma und 3 EL Semmelbrösel) und gut
durchrühren, so dass alles eine homogene Masse ergibt.

Am Ende mit Salz und Pfeffer abschmecken.

Buletten formen und in einer Pfanne braten. Die Buletten sollten
langsam gebraten werden, nicht zu große Hitze sonst verbrennen sie
aus und bleiben innen roh. (ca. 10min)

Schafskäse Buletten:

Semmelknödel laut Packungsangabe einweichen. Nicht aus den einzelnen
Verpackungen herausnehmen.

Fleisch in eine Schüssel geben und mit Salz und Pfeffer gut
verrühren.

Knoblauch und Zwiebeln schälen und klein hacken. Thymian und
Rosmarin abzupfen und klein schneiden.

Alle Zutaten, außer dem Schafskäse zum Fleisch geben und gut
durchrühren, so dass alles eine homogene Masse ergibt, Am Ende mit
Salz und Pfeffer abschmecken.

Buletten formen und in die Mitte ein Loch drücken. Ein Stückchen
Schafskäse in dieses Loch geben und dann wieder verschließen. Der
Schafskäse muss vom Fleisch umschlossen sein, so dass er beim Braten
nicht herausläuft.

