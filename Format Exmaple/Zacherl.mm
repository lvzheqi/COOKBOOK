     Title: MINISALAMI-FRITTATA MIT KARTOFFELN
Categories: Vorspeise, Chefkoch, Zacherl, Snack
     Yield: 4 Personen

      6    Eier
     10 ml Sahne
      2    Minisalami
      1 cn Kartoffeln
      1    Knoblauchzehe
    1/2    Zwiebel
    1/2 ts Kräuter der Provence
     80 g  Edamer
     50 ml Olivenöl
           Salz
           Pfeffer
           Muskat

MMMMM-----------------QUELLE----------------------
