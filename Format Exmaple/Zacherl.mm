     Title: GNOCCHI AL TRIFOLATI
Categories: Teigware, Gnocchi, Chefkoch, Zacherl
     Yield: 4 Personen

    400 g  Gnocchi
      4 tb Olivenöl
      1 tb (-1.5) Butter
      1 ts Gemüsebrühe
      4 tb Weißwein
    300 g  Egerlinge
    200 ml Gemüsebrühe
     24 sm Strauchtomaten
      2    Knoblauchzehen
      1    Zweig Rosmarin
      2    Zweige Thymian
    100 g  Parmesan
      2    Stängel Blattpetersilie
           Parmesan
           Salz
           Pfeffer
           Zucker

MMMMM-----------------QUELLE----------------------
           -- Ralf Zacherl "Berliner
           -- Meisterkoch 2002"
           -- Erfasst *RK* 25.08.04 von
           -- Peter Mess

Topf mit Wasser auf den Herd und salzen. Champignons putzen (Stiel
abschneiden und mit einem Tuch säubern), die Egerlinge vierteln oder
sechsteln, je nach Größe. Knoblauch schälen und klein hacken.

Strauchtomaten vierteln und das Kerngehäuse rausnehmen. (Wenn man
das Kerngehäuse drinlässt, dann müssen weniger Tomaten genommen
werden.) In der Zwischenzeit Gnocchi in das kochende Nudelwasser
geben.

Pfanne auf den Herd stellen. Gut erhitzen. Olivenöl in die Pfanne
und dazu die geviertelten Egerlinge. Wenn die Egerlinge hellbraun
sind, den Knoblauch, Rosmarin und Thymian hinzugeben und mit Salz,
Pfeffer und etwas Zucker abschmecken.

2 Minuten köcheln lassen. Dann alles mit Weißwein ablöschen. Mit der
Gemüsebrühe auffüllen und kochen lassen.

Wenn alles einmal aufgekocht hat, Tomaten und Butter hinzugeben. Das
ganze einkochen und abschmecken.

Blattpetersilie klein schneiden.

Gnocchi, wenn sie gar sind, abgießen und in die Pfanne hinzugeben.
Einmal durchschwenken und anrichten.

Am Ende Parmesan über die fertigen Gnocchi hobeln. Wenn es fertig
ist, schnell essen, dann schmeckt es am besten. Denn die Italiener
sagen: der Gast wartet auf die Nudeln, nicht die Nudeln auf den Gast!

