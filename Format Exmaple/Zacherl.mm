     Title: ZACHERLS ROTE BETE-SUPPE
Categories: Vorspeise, Suppe, Chefkoch, Zacherl
     Yield: 4 Personen

    400 g  Rote Beete gekocht
      2    Zwiebeln
    800 ml Gemüsefond
      3    Orangen
    200 ml Milch
           Salz
           Pfeffer
           Zucker
           Öl

MMMMM-----------------QUELLE----------------------
           -- Ralf Zacherl "Berliner
           -- Meisterkoch 2002"
           -- Erfasst *RK* 25.08.04 von
           -- Peter Mess

Orangen schälen und filetieren. 2 Orangen auspressen.

Topf aufsetzten, Öl dazu. Die geschälten und gewürfelte Zwiebeln
darin ansetzen. Zucker zu den Zwiebeln und dann bräunen lassen. Die
kleingeschnittene rote Bete hinzufügen. Mit dem O-Saft ablöschen.
Alles reduzieren bis der O-Saft fast verdunstet ist. Dann mit dem
Geflügelfond auffüllen und 10 min. köcheln lassen.

In der Zwischenzeit stellen wir die Suppentassen bei 60 °C in den
Ofen und erwärmen ein kleines Töpfchen Milch.

Mit einem Stabmixer die Suppe fein mixen. Mit Salz, Zucker und
Pfeffer abschmecken. Die Orangenfilets in die vorgewärmten
Suppentassen, die heiße Suppe darauf geben, nun die Milch aufmixen
und mit einem Esslöffel auf die Suppe geben.

