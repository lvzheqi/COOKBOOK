In der

Zwischenzeit den Thunfisch und die Scampis würfeln. Die
Tintenfischtube vierteln, dann in Streifen schneiden. Meeresfrüchte
in Schüssel geben, salzen, pfeffern. In Zitronensaft und Öl
marinieren.

Eine Bratpfanne aufsetzen, sehr gut erhitzen, die Meeresfrüchte kurz
braten und in die Gemüseboullabaise geben.

Abschmecken und genießen.

Vitaminsaft selbstgepresst oder gekauft dazu trinken.

