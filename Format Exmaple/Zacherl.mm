     Title: GESCHMORTE KALBSHAXE MIT RÖSTGEMÜSE UND KARTOFFELN
Categories: Fleisch, Kalb, Chefkoch, Zacherl
     Yield: 4 Personen

      1    Kalbshaxe
      1 sm Knollensellerie
      2    Knoblauchzehen
      8    (-12) Schalotten
      8    Möhren mit Grün
    1/2 l  Brühe
    1/2 l  Weißwein
           Salz
           Pfeffer
           Wacholderbeeren
     30 g  Salatkartoffel, klein und
           -- festkochend
           Salz
           Pfeffer
           Wacholderbeeren
           Lorbeerblätter
           Senfsaat
           Thymian

MMMMM-----------------QUELLE----------------------
           -- Ralf Zacherl "Berliner
           -- Meisterkoch 2002"
           -- Erfasst *RK* 25.08.04 von
           -- Peter Mess

Die Kalbshaxe parieren. Schalotten, Möhren und Sellerie schälen und
in walnussgroße Stücke schneiden. Kartoffeln waschen und halbieren
(bitte nicht schälen, geht schneller und schmeckt besser), dann auf
der Schnittfläche in Öl kurz anbraten (das Salzen nicht vergessen).

Die Haxe salzen, pfeffern und in Rapsöl scharf anbraten - jetzt bei
ca. 180° C in den vorgeheizten Backofen (kleine Schüssel mit Wasser
in den Backofen). Nach einer Stunde das Gemüse, die Aromaten und die
Kartoffeln zugeben. Wenn die Haxe goldbraun ist, mit Weißwein
ablöschen und mit Brühe auffüllen.

Nach weiteren 40 min. könnten Sie servieren, wenn Sie aber erst
später essen möchten, einfach Hitze reduzieren und später essen das
Essen wird nicht schlechter, nur noch besser!!! Falls Sie noch
später essen möchten, nehmen Sie das Gemüse raus, sonst wird es
matschig.

Um die Schwiegermutter ganz zu überzeugen: Haxe am Tisch tranchieren.

