Geschichte: Die Heimat der Paprikapflanze dürfte Kolumbien sein. Mit
dem Ende der zweiten Reise von Chri- stoph Columbus kamen erste
Sämlinge des »Pimiento de las Indias«, des indianischen Pfeffers,
nach Spanien. Von dort breitete sich die Pflanze gegen Ende des 16.
Jahrhunderts sehr schnell über die gemäßigten und warmen Klimazonen
Süd-Europas aus. Die Ungarn bemächtigten sich des roten
Nachtschattengewächses und kultivierten und veredelten es zur
Zuchtpflanze. Ungarischer Paprika gilt bei uns traditionell als
beste Qualität.

Hauptanbaugebiete: Heute liegen die bedeutensten Hauptanbaugebiete
in Ungarn, Spanien, Israel, Griechenland, Türkei, Jugoslawien, aber
auch in den gemäßigten, tropischen Zonen wie Afrika (RSA, Sambia,
Marokko) und Süd-Amerika (Brasilien, Chile).

Beschreibung: Der Anbau erfolgt feldmäßig. Die Paprikapflanzen sind
einjährig und bilden 60 - 100 cm verzweigte Büsche. Zum
Gewürzpaprika zählt man allgemein die länglichen, konischen oder
kegelförmigen, glatten, glänzenden, kräftig roten und nicht sehr
fleischigen, Beerenfrüchte, die zwischen 6 - 12 cm lang und bis zu 4
cm breit werden. Die Ernte erfolgt in der Regel per Hand, da die
Früchte zu unterschiedlicher Zeit voll ausreifen. Anschließend
werden die Schoten gewaschen, sortiert und maschinell getrocknet.
Die lebhaft rote Farbe des Paprikas wird durch das im Fruchtfleisch
enthaltene Capsanthin bestimmt, während die Schärfe des Geschmacks
von dem Alkaloid Capsaicin, kommt, das in den Scheidewänden und
Samen enthalten ist. Die Ungarn haben verschiedene Güte-Klassen,
wobei die gebräuchlichsten Handelssorten nachstehend aufgeführt
sind: Delikateßpaprika: lebhaft hell-, feuer- bis dunkelrot,
aromatisch-mild, fleischig-fruchtig. Er wird aus erstklassigen,
ausgesuchten, fehlerfreien und vollreifen Schoten hergestellt, wobei
die Scheidewände, Samen und Stiele entfernt werden. Edelsüßpaprika:
nicht so lebhaft rot, dunkel und intensivrot, mild, pikant,
aromatisch, süßlich-fruchtig. Ihm wird capsaicinfreier Samen
anteilmäßig zugeführt, der aufgrund seines hohen Fettgehaltes zu
einer gleichmäßigen Farbe führt. Rosenpaprika: wenig leuchtend,
dunkel- oder gelblich hellrot, scharf und charakteristisch
aromatisch. Das Rohmaterial wird mit den Scheidewänden und Samen
vermahlen.

Verwendung: Rohwürste, Fleisch-, Fisch-, Geflügel-, Reisgerichte,
braune Suppen und Saucen, Mix Pickles, Gemüse, Gulasch, Quark, Käse,
Salate, Marinaden.

Paprika ist nicht nur ein wohlschmeckendes Gewürz, sondern ist auch
für das Wohlbefinden gut: Paprika wirkt durchblutungsfördernd und
steigert die Nierenaktivität.
