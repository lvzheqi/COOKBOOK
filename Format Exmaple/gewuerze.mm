Honig-, Bienen-, Zitronenkraut, Herz- und Mutterkraut Der starke,
zitronenähnliche Duft, der beim Zerreiben von Blättern entsteht,
gibt über die Verwendbarkeit dieses aromatischen Kräut leins die
beste Auskunft. Leider ist es nur frisch zu verwenden und läßt sich
auch nicht erhitzen. Man gibt es daher in Soßen, an Mayonnaise,
Salate, Rohkost und Erfrischungsgetränke. Man soll die Blättchen nur
wenig schneiden oder hacken, damit nicht zuviel Duft und Geschmack
verloren gehen.

*Melissen-Butter An gerührte Butter gibt man Salz, etwas Pfefer,
einen Hauch Zucker und Zitronensaft, ein Gläschen Weinbrand und
rasch gehackte Zitronenmelisse. Die Butter wird zu Kalten oder
Käseplatten, zu Rohkost und zu Brot als Begleitung von Suppen
gereicht.

*Lachs-Sandwich Auf gebutterten Toast gibt man 1 - 2 Scheiben
Räucherlachs, 3 Rosen von Radieschen und dazwischen kleine Blättchen
von Zitronenmelisse.

*Bunter Salat Gleichviel Würfel von Käse und Apfel werden mit
Zitronensaft mariniert. Man gibt sie an grünen Salat und würzt mit
C51 oder Mayonnaise, wenig Zucker, Salz, etwas Peffer, etlichen
gehackten Kapern und viel gehackter Zitronenmelisse. Der Salat wird
mit einem gekochten, gehackten Ei überstreut.

*Zitronenquark An etwa 200 g Quark gibt man 2 - 3 Eßlöffel
geriebenen Parmesan, etwas Zitronensaft und Whisky, Salz, Pfeffer,
Muskat und reichlich gehackte Zitronenmelisse. Der Ouark wird mit
Bleichsellerie garniert.

*Melissen-Milch-Sekt In den Schüttelbecher gibt man 3 Eiswürfel, 3 -
4 Blätter Zitronenmelisse, 1/2 Likörglas Grenadine und 1/8 I kalte
Milch. Man schüttelt kurz aber kräftig, damit die Melisse ihr Aroma
abgeben kann. Dann füllt man mit 1 - 2 kleinen Flaschen Schaumwein
auf. Das Getränk wird in kleine Becher gegossen und mit je einem
Zweiglein Zitronenmelisse gerniert.

* Imperial In einen Schüttelbecher gibt man 2 - 3 Eiswürtel und 5 -6
Melissenblätter. Man schüttalt kräftig durch und gießt 1/2 Tasse
Himbaarsaft, den Saft von je einer Orange und Zitrone und 2 - 3
Eßlöffel Zucker dazu. Die nochmals geschüttelte Flüssigkeit wird in
hohe Limobecher abgegossen und langsam mit Mineralwasser aufgegossen.
Achtung, das Getränk schäumt jetzt stark!
