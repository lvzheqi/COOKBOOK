Botanisch (Lateinisch): Myristica fragrans Houtt. ú Englisch: Nutmeg
Französisch: Muscade, Noix de muscade

Geschichte: Die Muskatnuß war im Altertum den Griechen und Römern
nicht bekannt. In China und Indien geht der Gebrauch bis in die
vorchristliche Epoche zurück. Doch erst mit der Entdeckung des
Seeweges nach Indien durch Vasco da Gama, Anfang des

16. Jahrhunderts, und mit der Eroberung der Molukken 1511 durch den
portugiesischen Eroberer dAbreo begann die Muskatnuß ihren Siegeszug.
Die Holländer verdrängten 1605 die Portugiesen aus der indonesischen
Inselwelt und begründeten ihr Gewürzmonopol, indem sie den
Muskatnußanbau und -handel nur auf wenige Inseln, Banda, Ambon und
Ternate beschränkten. Sie vernichteten die Bestände auf den anderen
Inseln und verboten die Neuanpflanzungen. Bereits 1769 / 1770 gelang
es den Franzosen Samen und Schößlinge von Muskat- und Nelkenbäumen
zu entwenden und auf Runion und Mauritius anzupflanzen. 1802
vertrieben dann die Engländer die Holländer und sorgten für
weltweiten Anbau in vielen tropischen Gebieten.

Hauptanbaugebiete: Die heutigen Hauptanbaugebiete sind die
indonesische Inselwelt (ca. 70 %), Sri Lanka, Indien und Grenada (ca.
25 - 30 %).

Beschreibung: Der immergrüne Muskatnußstamm wird ca. 10 - 16 m hoch
und kann bis zu 100 Jahre alt werden. Die Bäume tragen ab dem 7. - 8.
Jahr aprikosenähnliche Früchte, die als Samenteile Macis und
Muskatnuß beinhalten. Der Muskatnußbaum bringt das ganze Jahr über
Früchte hervor, die zur Zeit der Vollreife hälftig aufspringen und
die Nuß, einen eiförmigen, braunen, sehr harten Samen, freigeben.
Dieser ist von einem auffällig karminrot gefärbten Arillus oder
Samenmantel = Muskatblüte, (Macis) umgeben. Nachdem das äußere
Fruchtfleisch entfernt worden ist, wird die Macis, die zur Ernte
noch recht fleischig ist, in Handarbeit vom Samen vorsichtig
entfernt und in der Sonne oder in Trockenräumen über mehrere Monate
getrocknet. Die Muskatblüte wird zwischendurch immer wieder
flachgedrückt und die karminrote Farbe verändert sich, je nach
Ursprung, in orange- bis gelbbraun, wobei sie hornartig fest wird.
Fertiggetrocknet kommt sie ganz oder gebrochen aus den Anbauländern.
Die Muskatblüte duftet und schmeckt so ähnlich wie die Muskatnuß,
ist aber noch feiner, zarter und aromatischer. Die von der Macis
befreiten Samen werden mehrere Wochen durch Sonneneinwirkung
getrocknet und sind verwendbar, wenn sich innen der Samen von der
