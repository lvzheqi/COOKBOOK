Lateinisch Botanisch: Capsicum frutescens L Englisch: Chillies,
Chili pepper Französisch: Poivre rouge, Piment en rag

Geschichte: Mit der Entdeckung der neuen Welt brachte Columbus neben
Paprika auch »AXI« (indianischer Name für Capsicum) mit nach Europa.
Aus peruanischen Gräberfunden weiß man, daß Capsicum-Arten schon in
der Prä-Inkazeit in der Diät der Indianer eine Rolle spielten. Der
Ursprung der Pflanze liegt im Amazonas-Quellgebiet. Die Spanier und
Portugiesen sorgten dann für weltweite Verbreitung des
Cayennepfeffers, Teufelspfeffers oder Spanischen Pfeffers, wie die
Chillies auch genannt werden.

Hauptanbaugebiete: Chillies gedeihen, bei weltweiter Verbreitung, in
den Tropen und auch in gemäßigten Zonen, wie Mittel- und Südamerika,
Indien, Sri Lanka, Pakistan, VR China, Thailand, Indonesien, Japan,
Türkei, Uganda, Nigeria, Tansania und in vielen anderen Ländern.

Beschreibung: Die Form und Größe der Chilischoten ist variantenreich.
Sie können von spitzkegelig (z.B. superscharfe ca. 1 cm kleine
Birdseye-Chili aus Papua Neuguinea oder Malawi) bis kugelig-rund (z.
B. Casabel aus Mexico oder Nordspanien) gedeihen. Die Schoten zeigen
im ausgereiften Zustand eine Farbenbreite von leuchtendrot über
orange bis glänzend braun. Obwohl die strauchartigen Pflanzen heute
größtenteils im Großanbau auf Feldern kultiviert werden, erfolgt die
Ernte noch per Hand, da die Schoten unterschiedlich reifen. Als
Gewürz werden die reifen, getrockneten Früchte ganz, grob zerstoßen
oder gemahlen in den Handel gebracht. Je nach Herkunft und Art
schmecken die Chillies mehr oder minder brennend-scharf und werden
daher auch als das Dynamit unter den Gewürzen betitelt.
Verantwortlich für die feurige Schärfe ist das Capsaicin,
Hauptbestandteil der Capsainnoid-Scharfstoffe. In der Regel sind die
kleinen, dickhäutigen und spitzen Chillies am schärfsten, während
die großen, runden und fleischigen Schoten milder im Geschmack sind.
So sind die spanischen Chillies mild pikant, während zum Beispiel
die mexikanische Küche kleine, scharfe Schoten (Morita, Chipotle,
Chile de Arbol) verwendet.

Verwendung: Für scharf und deftig schmeckende Gerichte, Fisch,
Chilisaucen, Curry, Gewürzgurken, Gulasch, Hackbraten, Gemüse,
Bohnen, Würzöle, für Wurstwaren wie Pusztastangen (rohe Debreziner),
Zigeunerkochsalami.

Es gibt eine erstaunliche Anzahl an Chiliarten: Ancho ist ein mildes,
dunkles Chili, Jalapeno ist dunkelgrün und sehr scharf, Mulato braun
und scharf, Pasilla lang, dünn, braun und scharf, Serrano klein,
grün und sehr scharf, Uganda- und Mombasa-Chillies ebenso. Gegen
Chiliverbrennungen helfen Milch, Joghurt und Eiscreme. Für Pharma-
Zwecke werden aus Chili-Extrakten muskelwärmende und
durchblutungsfördernde Salben hergestellt. Extrakte aus Chillies
wirken abstoßend auf eilegende Insekten.
