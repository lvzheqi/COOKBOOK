kurz erwärmt werden. Verwenden Sie die Blätter zu Fisch- und
Tomatengerichten, Suppen, Pasten und Kräutersaucen, in frischen
Salaten als auch zu Eier- und Nudelgerichten. Er paßt hervorragend
zu Pilzen und hellem Fleisch wie Kalb, Huhn oder Lamm. Besonders
bekannt ist Basilikum für seine Harmonie und geschmackliche
Unterstützung zu Tomaten. Außerdem darf er in
Kräuteressigzubereitungen nicht fehlen.

Die Heilkräfte von Basilikum : Basilikum enthält etwas Gerbstoff und
ätherische Öle (Estragol und Thymol), die sich wohltuend auf den
Magen- und Darmtrakt auswirken. Er hilft bei Völlegefühl nach gutem,
reichlichem Essen. Basilikumblätter unter die Zunge gelegt,
verbessern den Appetit, harmonisieren Nerven und Atemwege, wirken
krampflösend und beruhigend bei Blähungen und fördern die Verdauung.
