Basilikum blüht Juli bis August mit weißen, gelblichen oder auch
leicht rötlichen Blüten. Frischer Basilikum hält sich im Kühlschrank
nur etwa einen Tag, getrocknetes Kraut, kühl und dunkel gelagert,
einige Monate, verliert jedoch sehr im Geschmack.

Verwendet werden vor allem die Blätter, deren aromatischer Duft
Salate und feine Käsegerichte bereichert. Basilikum ist in Italien
eines der am häufigsten verwendeten Gewürze und Basis eines jeden
Pesto. Die getrockneten Blätter sind ein unverzichtbarer Bestandteil
