Wie Curry ist auch Garam-Masala eine indische Gewürz Mischung,
welche in ihrer Zusammensetzung je nach Region und Speise variiert.
Sie stammt aus Nordindien und ist dort weit verbreitet. Ein Masala
kann eine ganz einfache Mischung aus zwei oder drei Gewürzen und
Kräutern sein oder aber auch weitaus mehr beinhalten. Garam Masala
wird wesentlich sparsamer verwendet als Curry Mischungen und kann,
im Gegensatz zu Curry auch während oder am Ende der Garzeit an die
Speise gegeben werden. Die Gewürze können ganz oder gemahlen sein,
die Mischung kann scharf sein, dann basiert sie auf Pfeffer und
Nelken, oder mild, dann sind Macis, Zimt und Kardamom die
wesentlichen Geschmacksträger. Bei Prunkessen und zu Feierlichkeiten
werden gerne Rosenblätter mit den Gewürzen gegeben.

Eine würzig-schärfere Garam Masala Basismischung :
: 2 Zimtstangen
: 3 Lorbeerblätter
: 20g Kardamom
: 25g Koriander
: 20g schwarzer Pfeffer
: 40g Kreuzkümmel
: 15g Nelken
: 15g Macis

Fügen Sie noch ein paar Gewürze dazu, wenn Sie ein wirklich scharfes
Gujerati Masala möchten :

Gujerati Masala :
: Chilis
: Sesamsamen
: Fenchelsamen
: Ajowansamen

Parsi Dhansak Masala :
: Chilis
: Bockshornklee
: Senfkörner
: Kurkuma

Verdoppeln Sie zu diesem Masala den Koriander

Zwei milde Masalas mit unterschiedlichen Geschmacksrichtungen :

Moghul Masala :
: grüner Kardamom
: Zimt
: schwarze Pfefferkörner
: ein paar Nelken

Kashmiri Masala :
: schwarzer Kreuzkümmel
: grüner Kardamom
: schwarzer Pfeffer
: Nelken
: Zimt
: Macis
: etwas Muskatnuss gerieben

Die Gewürze (bis auf Macis) in einer heißen Pfanne bei mittlerer
Hitze trocken rösten, die Pfanne dabei immer in Bewegung halten und
leicht schütteln. Nun alles abkühlen lassen und den Macis hinzufügen.
Dann bzw. vor der Verwendung mit der Gewürzmühle (oder Kaffeemühle)
mahlen. Die Zimtstangen immer von Hand zerbröckeln, bevor sie in der
Pfanne geröstet werden. Sie können teilweise anstelle der Samen auch
die frischen Kräuter verwenden, wie beispielsweise den frischen
Koriander oder frische Chilischoten. Zu manchen Gerichten paßt auch
frische Minze sehr gut. Die Kräuter, bis auf die Chilischoten,
sollten Sie jedoch nicht mitgaren, sondern erst vor dem Servieren
hinzufügen.
