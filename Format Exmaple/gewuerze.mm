Lateinisch Botanisch: Syzygium aromaticum (L.) Merr. et Perry.
Englisch: Clove Französisch: Girofle

Geschichte: Bereits in der antiken chinesischen Literatur wurden die
Gewürznelken erwähnt. Damit ihr Atem gut rieche, durften die
chinesischen Staats- und Hofbeamten ihrem Kaiser nur mit ein paar
Nelken im Mund gegenübertreten. Erst mit der Blüte des Römischen
Reiches im 2. Jahrhundert, wurden die Gewürznelken in Europa bekannt.
Wahrscheinlich wurden sie von den Molukken durch malayische Händler
nach China und Indien gebracht und gelangten von dort über den
Seeweg und die alten Karawanenstraßen nach Alexandrien und
Konstantinopel. Bis ins Mittelalter hinein blieb Konstantinopel der
bedeutendste Handelsplatz für Gewürze. Nachdem die Portugiesen 1522
unter Magellan die Molukken Inseln eroberten, errichteten sie ihr
Gewürzmonopol, das ihnen ein Jahrhundert später von den Holländern
abgenommen wurde. Erst 1770 gelang es den Franzosen Nelkensämlinge
nach Mauritius und Ile de la Re£nion zu schmuggeln und anzubauen.
Von dort gelangten sie nach Zanzibar und Madagaskar. Heute ist man
überzeugt, daß der Ursprung des Gewürznelkenbaums auf den
indonesischen Batjan-Inseln (Molukken), zu suchen ist. Wie die
Muskatnußbäume gedeihen die Gewürznelken am besten im tropischen
Seeklima.

Hauptanbaugebiete: Heutige bedeutende Anbaugebiete sind Indonesien,
Sri Lanka, Madagaskar, Comoren, Malaysia, Brasilien, Tanzania
(Sansibar), sowie in bescheidenem Ausmaß auf den Westindischen
Inseln, Guyana und im Südwesten Indiens.

Beschreibung: Der immergrüne, zu den Myrtengewächsen gehörende
Gewürznelkenbaum wird ca. 15-20 m hoch, trägt ab dem 6. Jahr und
bringt seinen vollen Ertrag - je nach Größe bis zu 10 kg - zwischen
dem 20. und 25. Jahr. Die an den Zweigspitzen in Trugdolden
stehenden Blütenknospen sind ungefähr 12-18 mm lang und haben einen
vierkantig-gerundeten, 3-4 mm dicken, stielartig ausgebildeten
Fruchtknoten. Er hat über dem oberen Ende 4 zipfelig abstehende
Kelchblätter mit einem kugeligen Köpfchen (4-5 mm) aufweist. Sobald
die noch festgeschlossenen grünen Blütenknospen sich zu verfärben
beginnen, werden sie per Hand gepflückt oder mit Bambusstangen
abgeschlagen. Anschließend werden die Knospen auf Matten
ausgebreitet und in der Sonne getrocknet. Nach dem Trocknungsvorgang
verfärben sich die Blütenknospen rötlichbraun bis dunkelbraun. Sie
verlieren beträchtlich an Feuchtigkeit - 1.000 g frische Nelken
ergeben ca. 250 g getrocknete Nelken. Gute Gewürznelken geben etwas
Öl ab, wenn man sie mit dem Fingernagel drückt und sie sollten noch
ihr Köpfchen aufweisen. Nelken haben ein sehr stark ausgeprägtes,
charakteristisches Aroma und schmecken feurig würzig. Im Haushalt
werden meistens ganze Nelken verwendet, während die Industrie
gemahlene Ware bevorzugt. Indonesien, als größter Produzent und
Verbraucher, verarbeitet Nelken in seinen Zigaretten. Der
französische Name CLOU DE GIROFLE (Nelkennagel) trifft mit der
Beschreibung der Gewürznelke sozusagen den »Nagel auf den Kopf«. Das
Aroma wird vor allem durch das Eugenol geprägt, dessen nelkenartiges
Aroma wir aus der Zahnmedizin (Desinfektion) kennen.

Verwendung: Fleisch- und Fischgerichte, Wild, Lebkuchen, Glühwein,
Kompott, Zuckerwaren, Liköre, Getränke, Essigfrüchte, Marinaden,
Gemüse (Rotkohl), Suppen, Saucen, Kaltschale, Rote Beete,
Gewürzmischungen.

Ein altes Hausrezept: Bei Zahnschmerzen einfach eine Nelke auf den
schmerzenden Zahn legen und wie auf einem Kaugummi intensiv kauen.
Eine Schmerzlinderung stellt sich rasch ein. Das ätherische Öl wird
in der Medizin als schmerzstillendes bzw. lokalanästhesierendes und
wundheilendes Mittel verwendet. Die Araber bereiten aus Nelkenpulver
und Antimonsulfid eine aphrodisisch wirkende Salbe, KO HEUL, genannt.
Gewürznelken wirken in geringen Dosen anregend und magenstärkend.
