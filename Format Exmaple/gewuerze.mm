Alles begann in der Nähe von Mexico City im Jahre 6500 v.Chr. Dort
haben die Bewohner zum ersten Mal wilde Chilis gesammelt. Über den
Begin der Kultivierung der Chilis besteht noch keine vollständige
Klarheit. Die Zahlen reichen von 3400 v.Chr. bis 250 v.Chr. Damals
wurden die Chilis von Ureinwohnern als Gewürz und Gemüse verwandt.
In Peru wurden damals die Chilis sogar als Währung akzeptiert. Die
Chilis kamen mit Christoph Kolumbus nach Europa. Das war im Jahr
1492. Fälschlicherweise bekamen die mitgebrachten Chilipflanzen den
Namen pimenta nach dem spanischen Wort für Pfeffer. Erst der Arzt
Christoph Kolumbus', Dr. Alvarez Chanca, gab 1494 der Gattung den
noch heute gültigen Namen Capsicum. Im spanischen Klima gediehen die
mitgebrachten Schoten und wurden von dort ansässigen Mönchen
kultiviert. Kurioserweise wurden die Pflanzen anfangs als
Zierpflanze angesehen, aber nach und nach fand man Geschmack an den
Früchten. 50 Jahre nach der Entdeckung der Chilis wurden sie von dem
deutschen Medizinprofessor Leonard Fuch genauestens untersucht und
erforscht. Bekanntschaft mit den Chilis machte auch der deutsche
Hans Stade im Jahre 1550, der von den brasilianischen Ureinwohnern
gefangengenommen wurde. Im 18. Jahrhundert wurde Licht in das
Wirrwarr von Arten gebracht. Und zwar durch den schwedischen
Botaniker Carl von Linné. Er wies die Chilis eindeutig der Gattung
Capsicum zu. Die Portugiesen, die sich eher in Südamerika aufhielten,
beförderten die Chilis zuerst nach Afrika und von dort aus nach
Indien. Weiterhin wurden die Chilis von den Portugiesen auf ihren
Handelsrouten nach China, Korea, Japan und den Philippinen gebracht.
Heutzutage ist Indien der größte Chili Produzent der Welt. Und die
Chilis erfreuen sich immer größerer Beliebtheit. Die Chili Chronik

